package entrada_processamento_saida;

public class Ex_24_W {

	public static void main(String[] args) {
		// Exibir a tabuada do número cinco no intervalo de um a dez.

		int m, i;
			i = 1;
		
		while (i<=10) {
			
			m = 5 * i;
		
		System.out.printf("\n5 X %d = %d", i, m);
		i++;
	 }
   }
}
